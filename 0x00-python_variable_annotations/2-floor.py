#!/usr/bin/env python3

import math
'''Task: Implement a function that returns the floor of a given float number.
'''


def floor(n: float) -> int:
    """
    Returns the floor of a given float number.

    Args:
        n (float): The input float number.

    Returns:
        int: The floor of the input number.
    """
    return math.floor(n)
